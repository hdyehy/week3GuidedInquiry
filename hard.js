

//adds two numbers
function add(n0, n1) {
    return n0 + n1
}

//subtracts two numbers
function subtract(n0, n1) {
    return n0 - n1
}

//multiplies two numbers
function multiply(n0, n1) {
    return n0 * n1
}

//divides two numbers
function divide(n0, n1) {
    return n0 / n1
}

//example of every function working
console.log(add(45, 90), subtract(40, 41), multiply(2, 2), divide(33, 11))